    private void roundbutton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_roundbutton3ActionPerformed

        
        String mobile = textField1.getText();
        String fname = textField2.getText();
        String lname = textField3.getText();
        String email = textField4.getText();

        if (fname.isEmpty()) {
            Notifications.getInstance().show(Notifications.Type.WARNING,
                    Notifications.Location.TOP_CENTER, "Please enter first name");

        } else if (lname.isEmpty()) {
            Notifications.getInstance().show(Notifications.Type.WARNING,
                    Notifications.Location.TOP_CENTER, "Please enter last name");

        } else if (email.isEmpty()) {
            Notifications.getInstance().show(Notifications.Type.WARNING,
                    Notifications.Location.TOP_CENTER, "Please enter the email");
        } else if (!email.matches("^(?=.{1,64}@)[A-Za-z0-9\\+_-]+(\\.[A-Za-z0-9\\+_-]+)*@[^-][A-Za-z0-9\\+-]+"
                + "(\\.[A-Za-z0-9\\+-]+)*(\\.[A-Za-z]{2,})$")) {

            Notifications.getInstance().show(Notifications.Type.WARNING,
                    Notifications.Location.TOP_CENTER, "Please enter valid email");
        } else {

            try {

                ResultSet resultSet = Mysql.executeSearch("SELECT * FROM `supplier` WHERE `email` = '" + email + "' AND `mobile` !='" + mobile + "'");

                if (resultSet.next()) {

                    Notifications.getInstance().show(Notifications.Type.WARNING,
                            Notifications.Location.TOP_CENTER, "Email already used");
                } else {

                    Mysql.executeIUD("UPDATE `supplier` SET "
                            + "`fname` = '" + fname + "' , `lname` = '" + lname + "',`email` = '" + email + "' WHERE `mobile` = '" + mobile + "'");
                    reset();

                }
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
        
    }//GEN-LAST:event_roundbutton3ActionPerformed

