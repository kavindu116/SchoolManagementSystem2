    private void buttonGradient4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonGradient4ActionPerformed

        try {
            ResultSet resultSet = Mysql.executeSearch("SELECT * FROM `event` WHERE `event_status_id` = '1' ");
            if (resultSet.next()) {
                roundPanel3.removeAll();

                ViewEvents ve = new ViewEvents();
                roundPanel3.add(ve, BorderLayout.CENTER);

                SwingUtilities.updateComponentTreeUI(roundPanel3);
                
            } else {
                Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.TOP_CENTER, "No events!");
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_buttonGradient4ActionPerformed

