    private void loadClubs(String name) {

        try {

            ResultSet resultSet = Mysql.executeSearch("SELECT * FROM `clubs` INNER JOIN `employee` ON `clubs`.`employee_id` = `employee`.`id` WHERE `name` LIKE '" + name + "%'");

            DefaultTableModel model = (DefaultTableModel) table1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {

                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("id"));
                vector.add(resultSet.getString("name"));
                vector.add(resultSet.getString("employee.fname")+" "+resultSet.getString("employee.lname"));

                model.addRow(vector);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

