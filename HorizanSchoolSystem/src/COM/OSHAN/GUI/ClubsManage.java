    private void table1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table1MouseClicked
        if (evt.getClickCount() == 2) {

            int row = table1.getSelectedRow();

            EventsClubsManagement eventsClubsManagement = new EventsClubsManagement();

            try {
                String idN = String.valueOf(table1.getValueAt(row, 0));
                String clubName = String.valueOf(table1.getValueAt(row, 1));
                String person = String.valueOf(table1.getValueAt(row, 2));
                ClubRegister clubRegister = new ClubRegister(eventsClubsManagement, true, clubName, person, idN);
                clubRegister.setVisible(true);

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_table1MouseClicked

