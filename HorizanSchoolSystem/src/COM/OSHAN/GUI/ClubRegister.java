    private void loadPerson() {

        try {
            ResultSet resultSet = Mysql.executeSearch("SELECT * FROM `employee` WHERE `employee_type_id` = '2'");

            Vector<String> vector = new Vector<>();
            vector.add("Select");

            while (resultSet.next()) {
                vector.add(resultSet.getString("fname") + " " + resultSet.getString("lname"));
                personMap.put(resultSet.getString("fname") + " " + resultSet.getString("lname"), resultSet.getString("id"));

            }
            
            

            DefaultComboBoxModel model = new DefaultComboBoxModel(vector);
            jComboBox1.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

