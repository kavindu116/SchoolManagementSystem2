    private void buttonGradient1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonGradient1ActionPerformed

        String cName = textField1.getText();
        String person = String.valueOf(jComboBox1.getSelectedItem());

        if (cName.isEmpty()) {
            Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.BOTTOM_RIGHT, "Please enter Club Name");
        } else if (person.equals("Select")) {
            Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.BOTTOM_RIGHT, "Please Select Person in-charge");
        } else {

            try {
                ResultSet resultSet = Mysql.executeSearch("SELECT * FROM `clubs` WHERE `name` = '" + cName + "' ");

                if (resultSet.next()) {
                    Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.BOTTOM_RIGHT, "Club already Registeed!");
                } else {

                    Mysql.executeIUD("INSERT INTO `clubs`(`name`,`employee_id`) VALUES('" + cName + "','" + personMap.get(person) + "')");

                    this.dispose();
//                    reset();
//                    loadClubs(roundTextField1.getText());
                }
            } catch (Exception ex) {
                ex.printStackTrace();
            }

        }
    }//GEN-LAST:event_buttonGradient1ActionPerformed

