    private void loadStudentData() {
        try {
            ResultSet rs = Mysql.executeSearch("SELECT * FROM  student s"
                    + " INNER JOIN guardian gu ON s.guardian_mobile = gu.mobile"
                    + " INNER JOIN  class c ON s.class_id = c.id"
                    + " INNER JOIN  gender g ON s.gender_id = g.id"
                    + " INNER JOIN  intake i ON c.intake_id = i.id"
                    + " INNER JOIN  grade gr ON i.grade_id = gr.id "
                    + " INNER JOIN student_status st ON s.student_status_id = st.id WHERE s.id = '" + this.id + "'");

            if (rs.next()) {

                textField1.setText(rs.getString("id"));
                textField2.setText(rs.getString("fname") + " " + rs.getString("lname"));
                textField3.setText(rs.getString("date_of_birth"));
                textField4.setText(rs.getString("joined_date"));
                textField5.setText(rs.getString("c.name") + " - " + rs.getString("gr.name"));
                textField6.setText(rs.getString("intake_name"));
                textField7.setText(rs.getString("email"));
                textField8.setText(rs.getString("g.name"));
                textField9.setText(rs.getString("st.name"));
                textField10.setText(rs.getString("guardian_mobile"));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

