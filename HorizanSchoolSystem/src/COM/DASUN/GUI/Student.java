    private void roundbutton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_roundbutton1ActionPerformed
        try {
            // Retrieve user input from text fields
            String admission = textField1.getText();
            String studentName = textField2.getText();
            String DOB = textField3.getText();
            String joinedDate = textField4.getText();
            String grade = textField5.getText();
            String intake = textField6.getText();
            String email = textField7.getText();
            String gender = textField8.getText();
            String studentStatus = textField9.getText();
            String guardianMobile = textField10.getText();

            // Map parameters to the report
            HashMap<String, Object> params = new HashMap<>();
            params.put("Parameter1", admission);
            params.put("Parameter2", studentName);
            params.put("Parameter3", DOB);
            params.put("Parameter4", joinedDate);
            params.put("Parameter5", grade);
            params.put("Parameter6", intake);
            params.put("Parameter7", email);
            params.put("Parameter8", gender);
            params.put("Parameter9", studentStatus);
            params.put("Parameter10", guardianMobile);

            // Load the compiled .jasper report from resources
            InputStream s = this.getClass().getResourceAsStream("/reports/StudentReport.jasper");

            // Provide a dummy data source with 1 row (required for rendering)
            JREmptyDataSource dataSource = new JREmptyDataSource(1);

            // Fill the report with parameters and data source
            JasperPrint jasperPrint = JasperFillManager.fillReport(s, params, dataSource);

            // View the report
            JasperViewer.viewReport(jasperPrint, false);

            // Close the current form/window
            this.dispose();

        } catch (Exception e) {
            e.printStackTrace();
        }


    }//GEN-LAST:event_roundbutton1ActionPerformed

