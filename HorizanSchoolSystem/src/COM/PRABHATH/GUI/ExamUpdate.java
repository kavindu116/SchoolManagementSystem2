    private void buttonGradient1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonGradient1ActionPerformed

       
        String term = textField3.getText();
        String fee = textField4.getText();

        String status = String.valueOf(jComboBox1.getSelectedItem());

        try {
            
           if(fee.isEmpty()){
             Notifications.getInstance().show(Notifications.Type.WARNING,
             Notifications.Location.TOP_CENTER, "Add a Price");
 
        }
        
        else if(!fee.matches("\\d+(\\.\\d{1,2})?")){
            Notifications.getInstance().show(Notifications.Type.WARNING,
             Notifications.Location.TOP_CENTER, "Please enter a valid price (e.g., 123.45).");
        }else if(status.equals("SELECT")){
            Notifications.getInstance().show(Notifications.Type.WARNING,
             Notifications.Location.TOP_CENTER, "Select A Status");
     
        }else{
            Mysql.executeIUD("UPDATE `exams` SET `fee`='" + fee + "', `exam_status_id`='" + statusHashmap.get(status) + "' "
                    + "WHERE `exams`.`id`='" + exid + "'");

            Notifications.getInstance().show(Notifications.Type.SUCCESS,
            Notifications.Location.TOP_CENTER, "EXAM UPDATE SUCCEESSFULY");
        }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_buttonGradient1ActionPerformed

