    private void loaddata() {

        try {
            ResultSet resultSet = Mysql.executeSearch("SELECT * FROM `exams`"
                    + "INNER JOIN `grade` ON `exams`.`grade_id`=`grade`.`id`"
                    + "INNER JOIN `exam_term` ON `exams`.`exam_term_id`=`exam_term`.`id`"
                    + "INNER JOIN `exam_status` ON `exams`.`exam_status_id`=`exam_status`.`id`"
                    + "WHERE `exams`.`id`='" + exid + "'");

            if (resultSet.next()) {
                
                String dateString = resultSet.getString("year");
                LocalDate date1 = LocalDate.parse(dateString, DateTimeFormatter.ofPattern("yyyy-MM-dd"));
                int year = date1.getYear();
                textField1.setText(String.valueOf(year));

               // textField1.setText(resultSet.getString("year"));
                textField2.setText(resultSet.getString("grade.name"));
                textField3.setText(resultSet.getString("exam_term.term_name"));
                textField4.setText(resultSet.getString("fee"));
                String status = String.valueOf(resultSet.getString("exam_status.name"));
                jComboBox1.setSelectedItem(status);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void loadstatus() {

        try {

            ResultSet resultSet = Mysql.executeSearch("SELECT * FROM `exam_status`");

            Vector vector = new Vector();
            vector.add("SELECT");

            while (resultSet.next()) {

                vector.add(resultSet.getString("name"));
                statusHashmap.put(resultSet.getString("name"), resultSet.getString("id"));

            }
            DefaultComboBoxModel model = new DefaultComboBoxModel(vector);
            jComboBox1.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

